#!/usr/bin/env python3
"""Generate RESULTS.md from chiron eval JSONL files.

Usage: report.py <results.jsonl> [more.jsonl ...]

Prints markdown to stdout. Scores are the fraction of pre-registered
sub-checks passed per trial. Deltas (kit minus vanilla) get bootstrap 95%
confidence intervals (fixed seed, so the report is reproducible from the
same data). A directional claim is printed ONLY when the pooled CI
excludes zero; otherwise the report explicitly refuses one. If the
positive-control task does not separate the arms, all claims for that
model are voided (broken plumbing).
"""

import json
import random
import sys
from collections import defaultdict

BOOTSTRAP_ITERS = 10000
SEED = 1234
# Tasks need at least this many valid trials per arm to enter the pooled
# directional analysis; below it, bootstrap CIs degenerate (n=1 gives a
# zero-width interval and would fabricate certainty).
MIN_N = 3


def load(paths):
    rows = []
    for path in paths:
        with open(path, encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                try:
                    rows.append(json.loads(line))
                except ValueError:
                    print(f"report.py: skipping bad line in {path}",
                          file=sys.stderr)
    return rows


def bootstrap_ci(deltas_fn, n_iter=BOOTSTRAP_ITERS):
    """deltas_fn(rng) -> one resampled statistic; returns (lo, hi)."""
    rng = random.Random(SEED)
    stats = sorted(deltas_fn(rng) for _ in range(n_iter))
    return stats[int(0.025 * n_iter)], stats[int(0.975 * n_iter)]


def mean(xs):
    return sum(xs) / len(xs) if xs else float("nan")


def resample_mean_delta(kit, van):
    def stat(rng):
        k = [rng.choice(kit) for _ in kit]
        v = [rng.choice(van) for _ in van]
        return mean(k) - mean(v)
    return stat


def fmt(x, digits=2):
    if x is None or x != x:
        return "n/a"
    return f"{x:+.{digits}f}" if isinstance(x, float) else str(x)


def main(paths):
    rows = load(paths)
    if not rows:
        print("report.py: no data", file=sys.stderr)
        return 1

    out = []
    out.append("# chiron eval results")
    out.append("")
    out.append("Generated by `eval/report.py` from the JSONL files under "
               "`eval/results/`. Do not edit by hand; re-run "
               "`eval/report.sh` instead.")
    out.append("")
    out.append("## What this measures, and what it does not")
    out.append("")
    out.append("This eval answers one question: does installing the chiron "
               "kit move doctrine-targeted behaviors on a given model, and "
               "at what cost. It does NOT show that a weaker model becomes "
               "a stronger one. Single-prompt headless runs test doctrine "
               "content, not long-session delivery. Tasks and checks were "
               "written by the doctrine's author and were committed before "
               "the first scored run (pre-registration); overfitting to the "
               "task genre remains a real limitation. Scores are fractions "
               "of pre-registered mechanical sub-checks; no LLM judging.")
    out.append("")

    by_model = defaultdict(list)
    for r in rows:
        by_model[r.get("model_requested", "unknown")].append(r)

    for model in sorted(by_model):
        mrows = by_model[model]
        out.append(f"## Model: {model}")
        out.append("")
        cli_versions = sorted({r.get("cli_version", "?") for r in mrows})
        out.append(f"Claude Code version(s): {', '.join(cli_versions)}")
        out.append("")

        # Validity accounting.
        out.append("| outcome | count |")
        out.append("|---|---|")
        counts = defaultdict(int)
        for r in mrows:
            counts[r.get("outcome", "?")] += 1
        for k in sorted(counts):
            out.append(f"| {k} | {counts[k]} |")
        out.append("")

        valid = [r for r in mrows if r.get("outcome") == "completed"
                 and r.get("score") is not None]

        # Plumbing gate: positive-control must separate the arms.
        pc_kit = [r for r in valid if r["task"] == "positive-control"
                  and r["arm"] == "kit"]
        pc_van = [r for r in valid if r["task"] == "positive-control"
                  and r["arm"] == "vanilla"]
        # Delivery evidence: either doctrine-forced output marker separating
        # the arms proves the treatment reached the model. (Amended after
        # the smoke run and before the first scored run: haiku follows the
        # completion contract while omitting the pre-response block, so
        # gating on the pre-response marker alone would void real data.)
        plumbing_ok = True
        if pc_kit and pc_van:
            separated = False
            for marker in ("precheck-present", "completion-contract"):
                kit_rate = mean([1.0 if r["checks"].get(marker) else 0.0
                                 for r in pc_kit])
                van_rate = mean([1.0 if r["checks"].get(marker) else 0.0
                                 for r in pc_van])
                if kit_rate > van_rate:
                    separated = True
            plumbing_ok = separated
        elif pc_kit or pc_van:
            plumbing_ok = False

        tasks = sorted({r["task"] for r in valid})
        out.append("| task | vanilla mean (n) | kit mean (n) | delta | 95% CI |")
        out.append("|---|---|---|---|---|")
        per_task_deltas = {}
        for task in tasks:
            van = [r["score"] for r in valid
                   if r["task"] == task and r["arm"] == "vanilla"]
            kit = [r["score"] for r in valid
                   if r["task"] == task and r["arm"] == "kit"]
            if van and kit:
                delta = mean(kit) - mean(van)
                lo, hi = bootstrap_ci(resample_mean_delta(kit, van))
                if len(kit) >= MIN_N and len(van) >= MIN_N:
                    per_task_deltas[task] = (kit, van)
                ci = f"[{lo:+.2f}, {hi:+.2f}]"
            else:
                delta, ci = float("nan"), "n/a"
            out.append(
                f"| {task} | {mean(van):.2f} ({len(van)}) "
                f"| {mean(kit):.2f} ({len(kit)}) "
                f"| {fmt(delta)} | {ci} |")
        out.append("")

        # Pooled verdict with claim discipline.
        if not plumbing_ok:
            out.append("**PLUMBING FAILURE:** the positive-control task did "
                       "not separate the arms, meaning the treatment did "
                       "not demonstrably reach the model. All results for "
                       "this model are void; no claims are made.")
        elif not per_task_deltas:
            out.append(f"**No directional claim:** no task has at least "
                       f"{MIN_N} valid trials per arm; there is not enough "
                       f"data for a pooled analysis.")
        else:
            def pooled_stat(rng):
                deltas = []
                for kit, van in per_task_deltas.values():
                    k = [rng.choice(kit) for _ in kit]
                    v = [rng.choice(van) for _ in van]
                    deltas.append(mean(k) - mean(v))
                return mean(deltas)
            lo, hi = bootstrap_ci(pooled_stat)
            pooled = mean([mean(k) - mean(v)
                           for k, v in per_task_deltas.values()])
            out.append(f"Pooled delta (equal task weights): "
                       f"{pooled:+.3f}, 95% CI [{lo:+.3f}, {hi:+.3f}].")
            out.append("")
            if lo > 0:
                out.append("**Directional finding:** the kit arm scored "
                           "higher and the pooled CI excludes zero.")
            elif hi < 0:
                out.append("**Directional finding:** the kit arm scored "
                           "LOWER and the pooled CI excludes zero.")
            else:
                out.append("**No directional claim:** the pooled CI "
                           "includes zero at this sample size. The honest "
                           "summary is 'not enough data to say'.")
        out.append("")

        # Doctrine tax.
        out.append("### Cost and time (the doctrine tax)")
        out.append("")
        out.append("| task | arm | n | mean duration s | mean cost USD "
                   "| mean output tokens |")
        out.append("|---|---|---|---|---|---|")
        for task in tasks:
            for arm in ("vanilla", "kit"):
                sel = [r for r in valid
                       if r["task"] == task and r["arm"] == arm]
                if not sel:
                    continue
                dur = mean([r.get("duration_s") or 0 for r in sel])
                cost = [r.get("total_cost_usd") for r in sel
                        if r.get("total_cost_usd") is not None]
                tok = [r.get("output_tokens") for r in sel
                       if r.get("output_tokens") is not None]
                cost_s = f"{mean(cost):.4f}" if cost else "n/a"
                tok_s = f"{mean(tok):.0f}" if tok else "n/a"
                out.append(f"| {task} | {arm} | {len(sel)} | {dur:.0f} "
                           f"| {cost_s} | {tok_s} |")
        out.append("")

    print("\n".join(out))
    return 0


if __name__ == "__main__":
    if len(sys.argv) < 2:
        print(__doc__, file=sys.stderr)
        sys.exit(1)
    sys.exit(main(sys.argv[1:]))
